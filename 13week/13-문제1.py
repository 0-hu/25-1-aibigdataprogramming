# a = [1,2,3,4,5]
# 리스트 슬라이싱 예제

# a[1:3] -> [2, 3]
# a[3:5] -> [4, 5]
# a[:] -> [1,2,3,4,5]
# a[::] -> [4]
# a[3:-1] -> [4]
# a[::-1] -> [5,4,3,2,1]
# a[2::-1] -> [3,2,1]
# a[1:-2] -> [2,3]
# a[4:3] -> []

del_list=[4,7]
L = [1,3,4,4,3,2,7,4,1,7,4]

# for x in [4,7]: <- 짧은 리스트는 이렇게 심플하게도 가능
for x in del_list:
    while x in L:
        L.remove(x)

print(L)